@DataJpaTest
class UpdateOpenAnswerQuestionTest extends SpockTest {

    def question

    def setup() {

        given: "create a question"
        question = new Question()
        question.setCourse(externalCourse)
        question.setKey(1)
        question.setTitle(QUESTION_1_TITLE)
        question.setContent(QUESTION_1_CONTENT)
        question.setStatus(Question.Status.AVAILABLE)

        def questionDetails = new OpenAnswerQuestion()
        questionDetails.setSuggestion(SUGGESTION_1_CONTENT)
        question.setQuestionDetails(questionDetails)
        questionDetailsRepository.save(questionDetails)
        questionRepository.save(question)

    }

    def "update a question"() {
        given: "a changed question"
        def questionDto = new QuestionDto(question)
        questionDto.setQuestionDetailsDto(new OpenAnswerQuestionDto())

        and: "a changed suggestion"
        questionDto.getQuestionDetailsDto().setSuggestion(SUGGESTION_2_CONTENT)

        when:
        questionService.updateQuestion(question.getId(), questionDto)

        then: "the question is changed"
        questionRepository.count() == 1L
        def result = questionRepository.findAll().get(0)
        result.getQuestionDetails().getSuggestion() == SUGGESTION_2_CONTENT
    }

    def "update question with missing suggestion"() {
        given: "a question"
        def questionDto = new QuestionDto(question)
        questionDto.setQuestionDetailsDto(new OpenAnswerQuestionDto())

        and: "a invalid suggestion"
        questionDto.getQuestionDetailsDto().setSuggestion(invalidSuggestion)

        when:
        questionService.updateQuestion(question.getId(), questionDto)

        then: "the question an exception is thrown"
        def exception = thrown(TutorException)
        exception.getErrorMessage() == ErrorMessage.INVALID_CONTENT_FOR_SUGGESTION

        where:
        invalidSuggestion << [null, SUGGESTION_EMPTY_CONTENT]
    }

    @TestConfiguration
    static class LocalBeanConfiguration extends BeanConfiguration {}
}